#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr;
	size_t	i;

	ptr = (char*)b;
	i = -1;
	while (++i < len)
		ptr[i] = (char)c;
	return (b);
}
